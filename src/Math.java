public class Math {
    public static void sum(double g, double h) {
        double c = g + h;
        System.out.printf("%.4f", c);
    }
    public static void diff(double g, double h) {
        double c = g - h;
        System.out.printf("%.4f", c);
    }
    public static void mult(double g, double h) {
        double c = g * h;
        System.out.printf("%.4f", c);
    }
    public static void div(double g, double h) {
        double c = g / h;
        System.out.printf("%.4f", c);
    }
}
